install_tool() {
    local tool_name=$1
    local install_command=$2
    local check_command=$3

    if ! command -v $check_command &> /dev/null; then
        echo "Installation $tool_name..."
        eval $install_command
    else
        echo "$tool_name Installed."
    fi
}


install_package() {
    local package_name=$1
    if ! dpkg -l | grep -qw $package_name; then
        echo "Installation $package_name..."
        sudo apt-get update
        sudo apt-get install -y $package_name
    else
        echo "$package_name Installed."
    fi
}

